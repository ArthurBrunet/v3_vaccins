                Les voyages lointains, ça se prépare
                Vous avez prévu de partir en voyage ? L’excitation ne doit pas faire oublier que la belle saison est également celle des accidents. Pour vous en prémunir, il est indispensable de bien vous préparer. Surtout si vous partez pour une destination lointaine. Voici quelques précautions utiles avant le grand départ …
                Avant tout départ, vérifiez que vous êtes bien à jour de vos vaccinations de base : tétanos, polio, hépatite B, coqueluche, diphtérie, ROR (rougeole, oreillons, rubéole)…  Pour les autres, n’oubliez pas que les pays ne sont pas égaux en matière de santé publique ! Voilà pourquoi certaines vaccinations sont plus spécialement recommandées dans certaines régions du monde. En cas de doute adressez-vous à votre médecin.

                Vous souffrez d’une maladie chronique telle que le diabète, l’asthme, l’hypertension artérielle, une maladie cardiaque ou un excès de cholestérol ?  La plupart des destinations exotiques vous restent accessibles. Même lorsque la maladie est bien stabilisée, l’avion, la longueur du vol et l’altitude peuvent modifier cet état de stabilité. Il est donc fortement recommandé de demander l’avis à son médecin, afin que toutes les précautions soient prises.

                En cas de diabète insulinodépendant, vous devrez ajuster votre traitement en fonction des fuseaux horaires. Au-delà de trois heures de décalage, votre médecin vous aidera à aménager les horaires d’injection sans pour autant modifier le protocole thérapeutique. Vous changerez le rythme des injections d’insuline le jour du départ et, pendant quelques jours, augmenterez le rythme des auto-contrôles glycémiques.  Emportez quelques doses de sucre ! Si un incident venait à déplacer l’heure d’un repas, elles vous permettront de prévenir un accident hypoglycémique.

                Enfin, certains documents peuvent être aussi importants que votre passeport : votre carte de diabétique ; une ordonnance de secours et un certificat médical rédigé en Anglais. Il attestera votre maladie et la nécessité de transporter des seringues !

